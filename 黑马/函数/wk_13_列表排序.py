name_list = ["zhangsan", "lisi", "wangwu"]
name_num = [1, 5, 4, 12, 3]

# 升序
name_list.sort()
name_num.sort()
print(name_list)
print(name_num)

# 降序
name_list.sort(reverse=True)
name_num.sort(reverse=True)
print(name_list)
print(name_num)

# 逆序(反转)
name_list.reverse()
name_num.reverse()
print(name_list)
print(name_num)