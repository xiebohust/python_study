poem = "大漠孤烟直,长河落日圆"

print(poem[2:4])  # 孤烟
print(poem[2:])  # 孤烟直,长河落日圆
print(poem[:-1])  # 大漠孤烟直,长河落日
print(poem[:])  # 大漠孤烟直,长河落日圆
print(poem[-1])  # 圆
print(poem[0::-1])  # 大
print(poem[-1::-1])  # 圆日落河长,直烟孤漠大
print(poem[::-1])  # 圆日落河长,直烟孤漠大
print(poem[::2])  # 大孤直长落圆
print(poem[::-2])  # 圆落长直孤大


